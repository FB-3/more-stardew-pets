class Decoration extends GameObject {

    //Constructor
    constructor(config, preset) {
        //Add sprite to config
        config.sprite = `decoration/decoration.png`;

        //Create game object
        super({ ...config, ...preset });

        //Add to decoration list
        Game.decoration.push(this);
    }

    remove() {
        super.remove();

        //Remove from decoration list
        Game.decoration.removeItem(this);
    }

}